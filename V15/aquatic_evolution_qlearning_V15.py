class QTabular:
    """
    Tabular Q-learning with state discretization.
    
    The Q-table is a dictionary:
    - Key: tuple of N segment angles (the state)
    - Value: array of Q-values, one per action (size = 3*N + 2 if variable segments)
    
    For N segments:
    - State dimension: N (one angle per segment)
    - Action dimension: 3*N (+ 2 for add/remove if variable segments)
    """

    def __init__(self, 
                 state_dim, action_dim,
                 learning_rate=0.15):

        # Dimensions
        self.state_dim  = state_dim   # Number of segments (state = N segment angles)
        self.action_dim = action_dim  # Number of actions = 3*N (+ 2 if variable segments)

        self.q_table = {}             # Q-table: {state_tuple: Q-values array}
        self.learning_rate = learning_rate

    def get_q_values(self, state):
        """Retrieve the Q-values array for a given state."""

        # Convert state to tuple (hashable for dictionary key)
        state_key = tuple(np.round(state, 4))  # Round to avoid floating point issues

        # Initialize Q-values to 0 if state not seen before
        if state_key not in self.q_table:
            self.q_table[state_key] = np.zeros(self.action_dim)
        
        return self.q_table[state_key]
    
    def update_q_values(self, state, action, target):
        """
        Update a single Q-value using the Q-learning update rule:
        Q(s,a) = Q(s,a) + learning_rate * (target - Q(s,a))
        """

        state_key = tuple(np.round(state, 4))

        if state_key not in self.q_table:
            self.q_table[state_key] = np.zeros(self.action_dim)

        # Q-learning update
        self.q_table[state_key][action] = self.q_table[state_key][action] + self.learning_rate * (target - self.q_table[state_key][action])


class QNetwork(nn.Module):
    """
    Standard Neural Network for Q-value approximation.
    
    Input: state (N segment angles)
    Output: Q-values for all actions (3*N + 2 values if variable segments)
    
    The network learns to approximate Q(s,a) for all actions a given state s.
    """
    
    def __init__(self, state_dim, action_dim):

        super(QNetwork, self).__init__()
        
        self.state_dim = state_dim
        self.action_dim = action_dim
        
        self.network = nn.Sequential(
            nn.Linear(state_dim, 256),
            nn.LayerNorm(256),
            nn.ReLU(),
            nn.Linear(256, 256),
            nn.LayerNorm(256),
            nn.ReLU(),
            nn.Linear(256, 128),
            nn.ReLU(),
            nn.Linear(128, action_dim)
        )
        
        # Initialize weights
        for m in self.network:
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.zeros_(m.bias)
    
    def get_q_values(self, state):
        """
        Get Q-values for all actions given a state.
        Returns: numpy array of Q-values (one per action)
        """
        with torch.no_grad():
            state_tensor = torch.from_numpy(state).float() if isinstance(state, np.ndarray) else state.float()
            return self.network(state_tensor).numpy()
    
    def forward(self, x):
        """Forward pass through the network."""
        return self.network(x)


class FlexibleQNetwork(nn.Module):
    """
    Flexible Neural Network that can adapt input/output dimensions while preserving hidden weights.
    
    This network preserves the hidden layer weights when the input/output dimensions change,
    allowing learned features to be retained across dimension changes.
    
    Architecture: Linear -> LayerNorm -> ReLU -> Linear -> LayerNorm -> ReLU -> Linear -> ReLU -> Linear
    """
    
    def __init__(self, state_dim, action_dim):
        super(FlexibleQNetwork, self).__init__()
        
        self.state_dim = state_dim
        self.action_dim = action_dim
        
        # Define layers individually for flexibility
        self.input_layer = nn.Linear(state_dim, 256)
        self.ln1 = nn.LayerNorm(256)
        self.hidden1 = nn.Linear(256, 256)
        self.ln2 = nn.LayerNorm(256)
        self.hidden2 = nn.Linear(256, 128)
        self.output_layer = nn.Linear(128, action_dim)
        
        # Initialize weights
        self._init_weights()
    
    def _init_weights(self):
        """Initialize network weights using Xavier initialization."""
        for layer in [self.input_layer, self.hidden1, self.hidden2, self.output_layer]:
            nn.init.xavier_uniform_(layer.weight)
            nn.init.zeros_(layer.bias)
    
    def forward(self, x):
        """Forward pass through the network."""
        x = self.input_layer(x)
        x = self.ln1(x)
        x = torch.relu(x)
        x = self.hidden1(x)
        x = self.ln2(x)
        x = torch.relu(x)
        x = self.hidden2(x)
        x = torch.relu(x)
        x = self.output_layer(x)
        return x
    
    def get_q_values(self, state):
        """Get Q-values for all actions given a state."""
        with torch.no_grad():
            state_tensor = torch.from_numpy(state).float() if isinstance(state, np.ndarray) else state.float()
            return self.forward(state_tensor).numpy()
    
    def adapt_dimensions(self, new_state_dim, new_action_dim):
        """
        Adapt the network to new input/output dimensions while preserving hidden weights.
        
        This method:
        - Preserves all hidden layer weights completely
        - For input layer: copies old weights and initializes new dimensions with small random values
        - For output layer: copies overlapping weights and initializes new dimensions with small random values
        
        Returns: self (for method chaining)
        """
        if new_state_dim == self.state_dim and new_action_dim == self.action_dim:
            return self  # No change needed
        
        # Create new input layer
        new_input = nn.Linear(new_state_dim, 256)
        with torch.no_grad():
            # Copy overlapping weights
            min_state = min(self.state_dim, new_state_dim)
            new_input.weight[:256, :min_state] = self.input_layer.weight[:, :min_state]
            new_input.bias[:] = self.input_layer.bias[:]
            # Initialize new input dimensions (if expanded)
            if new_state_dim > self.state_dim:
                nn.init.xavier_uniform_(new_input.weight[:, self.state_dim:])
        
        # Create new output layer
        new_output = nn.Linear(128, new_action_dim)
        with torch.no_grad():
            # Copy overlapping weights
            min_action = min(self.action_dim, new_action_dim)
            new_output.weight[:min_action, :] = self.output_layer.weight[:min_action, :]
            new_output.bias[:min_action] = self.output_layer.bias[:min_action]
            # Initialize new output dimensions (if expanded)
            if new_action_dim > self.action_dim:
                nn.init.xavier_uniform_(new_output.weight[self.action_dim:, :])
                nn.init.zeros_(new_output.bias[self.action_dim:])
        
        # Replace layers
        self.input_layer = new_input
        self.output_layer = new_output
        
        # Update dimensions
        self.state_dim = new_state_dim
        self.action_dim = new_action_dim
        
        return self


class LearningProcess:

    """
    Q-learning implementation supporting both neural network and tabular approaches.

    State: segment angles (N values for N segments)
    Actions: open/close/hold for each segment = 3*N actions (+ add/remove if variable = 2 more)
    
    Q-learning update: Q(s,a) = Q(s,a) + learning_rate * (target - Q(s,a))
    where target = cost + gamma * min_a' Q(s', a')
    """

    def __init__(self,
                 state_dim, action_dim,
                 use_nn=False, 
                 learning_rate_nn=0.001, learning_rate_tabular = 0.15,
                 gamma = 0.95,
                 coeff_dist=0.05, coeff_vel=0.5, coeff_energy=0.005,
                 epsilon_min=0.05):
        
        # Dimensions
        self.state_dim  = state_dim   # Number of segments (state = N segment angles)
        self.action_dim = action_dim  # Number of actions = 3*N (+ 2 if variable segments)

        # Whether to use or not a neural network
        self.use_neural_network = use_nn

        # If yes, initializes the Neural Network and use ADAM as optimizer
        if use_nn:
            self.model     = FlexibleQNetwork(state_dim, action_dim)
            self.optimizer = optim.Adam(self.model.parameters(), lr=learning_rate_nn)

        # Otherwise, initializes the Tabular model
        else:
            self.model     = QTabular(state_dim, action_dim, learning_rate=learning_rate_tabular)
            self.optimizer = None

        # Learning rates
        self.learning_rate_nn      = learning_rate_nn
        self.learning_rate_tabular = learning_rate_tabular

        # Discount factor
        self.gamma = gamma

        # Coefficients for objective function
        self.coeff_dist   = coeff_dist
        self.coeff_vel    = coeff_vel
        self.coeff_energy = coeff_energy

        # Minimum epsilon for epsilon-greedy strategy
        self.epsilon_min = epsilon_min

    def update_dimensions(self, new_state_dim, new_action_dim):
        """Update model dimensions when segment count changes.
        
        IMPORTANT: 
        - For tabular Q-learning, we preserve existing Q-values for states that are still valid.
        - For neural network, we use FlexibleQNetwork which preserves hidden layer weights.
        """
        
        if new_state_dim == self.state_dim and new_action_dim == self.action_dim:
            return  # No change needed
        
        old_state_dim = self.state_dim
        old_action_dim = self.action_dim
        
        # Update dimensions
        self.state_dim = new_state_dim
        self.action_dim = new_action_dim
        
        if self.use_neural_network:
            # Use FlexibleQNetwork's adapt_dimensions to preserve hidden weights
            self.model.adapt_dimensions(new_state_dim, new_action_dim)
            # Recreate optimizer with new parameters
            self.optimizer = optim.Adam(self.model.parameters(), lr=self.learning_rate_nn)
        else:
            # For tabular Q-learning, preserve existing Q-values
            old_q_table = self.model.q_table.copy()
            self.model = QTabular(new_state_dim, new_action_dim, learning_rate=self.learning_rate_tabular)
            
            # Copy over Q-values for states that are still valid
            for state_key, q_values in old_q_table.items():
                # Handle state dimension change
                if new_state_dim > old_state_dim:
                    # Segment added - pad state with 0
                    new_state_key = state_key + (0.0,) * (new_state_dim - old_state_dim)
                elif new_state_dim < old_state_dim:
                    # Segment removed - truncate state
                    new_state_key = state_key[:new_state_dim]
                else:
                    new_state_key = state_key
                
                # Copy Q-values for overlapping actions
                new_q = np.zeros(new_action_dim)
                min_actions = min(old_action_dim, new_action_dim)
                new_q[:min_actions] = q_values[:min_actions]
                
                self.model.q_table[new_state_key] = new_q

    def update_q_values(self, state, action, cost, next_state):
        """Update Q-values based on experience.

           The formula is: Q(S,A) = Q(S,A) + learning_rate * [ (C + gamma * min_A' Q(S',A') ) - Q(S,A) ]
           where:
                - S/A are the current state/action
                - S' is the next state the agent moves to
                - A' is the best next action in state S'
                - gamma is the discount factor
                - C is the cost received for taking action A in state S
        """

        # If we use a neural network...
        if self.use_neural_network:
            with torch.no_grad():
                q_next = self.model.get_q_values(next_state)                      # Retrieve Q-values for next state
                target = cost + self.gamma * np.min(q_next)                       # Compute target = C + gamma * min_A' Q(S',A')
            
            # Compute loss and update weights
            state_tensor = torch.from_numpy(state).float()
            q_values = self.model.forward(state_tensor)
            current_q = q_values[action]
            target_tensor = torch.tensor(target, dtype=torch.float32)
            loss = nn.MSELoss()(current_q, target_tensor)
            
            self.optimizer.zero_grad()
            loss.backward()
            nn.utils.clip_grad_norm_(self.model.parameters(), 1.0)
            self.optimizer.step()

        # Otherwise...
        else:
            q_next = self.model.get_q_values(next_state)                          # Retrieve Q-values for next state
            target = cost + self.gamma * np.min(q_next)                           # Compute target = C + gamma * min_A' Q(S',A')
            self.model.update_q_values(state, action, target)                     # Update Q-table

    def calculate_reward(self, creature, energy_cost):
        """Calculate reward based on creature state."""

        # Retrieve the current distance wandered from the start along the desired direction,
        # Based on the current head position
        distance = np.dot(creature.head_pos - creature.start_pos, creature.direction_vector)

        # Retrieve the future distance wandered in addition along the desired direction,
        # Based on the current head velocity
        speed     = np.linalg.norm(creature.head_vel)
        alignment = np.dot(creature.head_vel / (speed+1e-8), creature.direction_vector)

        # Store those information
        info = {'distance': distance,'speed': speed,'alignment': alignment}
        
        # Calculate reward components
        reward_speed    = alignment * speed * self.coeff_vel
        reward_distance = distance * self.coeff_dist
        penalty_energy  = energy_cost * self.coeff_energy
        reward          = reward_speed + reward_distance - penalty_energy
        
        return reward, info
    
    def select_action(self, state, epsilon):
        """Select action using epsilon-greedy strategy."""

        # Generate a random value between 0 and 1,
        # if it's less than epsilon, we explore (random action)
        if random.random() < epsilon:
            return random.randint(0, self.action_dim - 1)
        
        # Otherwise,
        # we exploit the learned values by choosing the action with the lowest Q-value for the current state
        else:

            # If we use a neural network...
            if self.use_neural_network:
                with torch.no_grad():
                    q_values = self.model.get_q_values(state)
                    return int(np.argmin(q_values))
            
            # Otherwise...
            else:
                q_values = self.model.get_q_values(state)
                return int(np.argmin(q_values))
            
    def get_epsilon(self, episode, total_episodes):
        """Calculate current epsilon with linear decay."""
        return max(self.epsilon_min, 1.0 - (episode / total_episodes) * (1.0 - self.epsilon_min))
    
    def get_model_state(self):
        """Get current model state for checkpointing."""
        if self.use_neural_network:
            return self.model.state_dict()
        else:
            # Convert numpy arrays to lists for serialization
            return {k: v.tolist() for k, v in self.model.q_table.items()}
    
