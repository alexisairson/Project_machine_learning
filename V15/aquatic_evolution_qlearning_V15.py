def save_simulation(checkpoints, use_nn, target_angle, duration, num_segments, fixed_segments, script_dir):
    """Save simulation results to file."""

    result_dir = ensure_result_folder(script_dir)
    timestamp  = datetime.now().strftime("%Y%m%d_%H%M%S")
    
    if use_nn:
        filename = f"simulation_nn_{timestamp}.pkl"
    else:
        filename = f"simulation_tabular_{timestamp}.pkl"
    
    filepath = os.path.join(result_dir, filename)
    
    # Convert checkpoints to serializable format
    serializable_checkpoints = []
    for cp in checkpoints:
        cp_dict = {
            'episode': cp.episode,
            'model_state': cp.model_state,
            'trajectory': cp.trajectory,
            'max_distance': cp.max_distance,
            'use_nn': cp.use_nn,
            'num_segments': cp.num_segments
        }
        serializable_checkpoints.append(cp_dict)
    
    save_data = {
        'checkpoints': serializable_checkpoints,
        'target_angle': target_angle,
        'duration': duration,
        'num_segments': num_segments,
        'fixed_segments': fixed_segments,
        'use_nn': use_nn
    }
    
    try:
        with open(filepath, 'wb') as f:
            pickle.dump(save_data, f, protocol=pickle.HIGHEST_PROTOCOL)
        print(f"Simulation saved to: {filepath}")
        return filepath
    except Exception as e:
        print(f"Error saving simulation: {e}")
        return None

